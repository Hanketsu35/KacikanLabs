a = int(input("First: "))
b = int(input("Second: "))

print(str(a) + " + "  + str(b) + " = " + str(a + b))
print(str(a) + " - "  + str(b) + " = " + str(a - b))
print(str(a) + " * "  + str(b) + " = " + str(a * b))
print(str(a) + " / "  + str(b) + " = " + str(a / b))
print(str(a) + " // " + str(b) + " = " + str(a // b))
print(str(a) + " % "  + str(b) + " = " + str(a % b))
print(str(a) + " ** " + str(b) + " = " + str(a ** b))
